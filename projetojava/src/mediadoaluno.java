// 1. Crie um programa que realize a média de duas notas decimais e exiba o resultado.

 class mediadoaluno {
    public static void main (String [] args) {

        double nota1 = 5.2;
        double nota2 = 9.3;

        double media = (nota1 + nota2) / 2;

        System.out.println("A nota do aluno é: " + media);

    }
}

